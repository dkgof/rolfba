
package rge.importing;

/**
 *
 * @author Rolf
 */
public class Point2 {
    private double s;
    private double t;

    public Point2(double s, double t) {
        this.t = t;
        this.s = s;
    }

    /**
     * @return the s
     */
    public double getS() {
        return s;
    }

    /**
     * @return the t
     */
    public double getT() {
        return t;
    }
}
